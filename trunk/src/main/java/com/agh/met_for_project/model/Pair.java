package com.agh.met_for_project.model;



public class Pair {

    private Object val1;
    private Object val2;

    public Pair(Object val1, Object val2) {
        this.val1 = val1;
        this.val2 = val2;
    }

    public Object getVal1() {
        return val1;
    }

    public Object getVal2() {
        return val2;
    }
}
